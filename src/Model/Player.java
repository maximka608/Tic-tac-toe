package Model;

public class Player {
    private String username;
    private String symbol;


    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public void setSymbol(String s) {
        this.symbol = s;
    }

    public String getSymbol() {
        return symbol;
    }

}
